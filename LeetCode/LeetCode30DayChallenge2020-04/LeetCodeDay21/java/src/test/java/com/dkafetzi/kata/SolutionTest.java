		LOGGER.debug("Simple test 1");

		/*
		 * Example 1:
		 * Input: mat = [[0,0],[1,1]]
		 * Output: 0
		 * 
		 */
		Solution solution = new Solution();
		int[][] input = {
				{0, 0},
				{1, 1}
		};
